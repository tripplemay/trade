"""B047 F004 — investment_report table.

One row per canonical investment report: a Master Portfolio (or sleeve)
backtest run with default parameters, generated by the canonical job
(``workbench_api/backtests/canonical.py``) which imports ``trade`` and runs the
real engine + report generation. The user-facing Reports page reads these
(``kind='investment'``) — the development sign-offs under ``docs/test-reports/``
are NOT in the user Reports list (they remain reachable via the /api/docs deep
links). Upserted by ``(strategy_id, as_of_date)`` — one authoritative report
per strategy per signal date.

These are HISTORICAL backtest reports, never a forward return prediction
(positioning §1.1).
"""

from __future__ import annotations

from datetime import date, datetime
from typing import Any
from uuid import UUID

from sqlalchemy import Date, DateTime, Index, String, Text, UniqueConstraint
from sqlalchemy.dialects.postgresql import JSONB
from sqlalchemy.orm import Mapped, mapped_column
from sqlalchemy.types import JSON

from workbench_api.db.models.base import Base
from workbench_api.db.models.news import _UuidString

KIND_INVESTMENT = "investment"


class InvestmentReport(Base):
    __tablename__ = "investment_report"
    __table_args__ = (
        UniqueConstraint(
            "strategy_id", "as_of_date", name="uq_investment_report_strategy_date"
        ),
        Index("ix_investment_report_slug", "slug"),
        Index("ix_investment_report_as_of_date", "as_of_date"),
    )

    id: Mapped[UUID] = mapped_column(_UuidString(), primary_key=True)
    slug: Mapped[str] = mapped_column(String(96), nullable=False)
    strategy_id: Mapped[str] = mapped_column(String(64), nullable=False)
    as_of_date: Mapped[date] = mapped_column(Date, nullable=False)
    title: Mapped[str] = mapped_column(String(200), nullable=False)
    markdown: Mapped[str] = mapped_column(Text, nullable=False)
    metrics_json: Mapped[Any | None] = mapped_column(
        JSON().with_variant(JSONB(), "postgresql"), nullable=True
    )
    kind: Mapped[str] = mapped_column(String(32), nullable=False, default=KIND_INVESTMENT)
    computed_at: Mapped[datetime] = mapped_column(
        DateTime(timezone=True), nullable=False
    )

    def __repr__(self) -> str:
        return (
            f"InvestmentReport(slug={self.slug!r}, strategy_id={self.strategy_id!r}, "
            f"as_of_date={self.as_of_date!r})"
        )
