"""B043 F002 — grounded "why this Sharpe/drawdown" explanation for a backtest.

Generated by the worker (off the request path, §12.10.2) from the real,
already-computed result the runner returns — the metrics (cagr / sharpe /
max_drawdown / turnover), the trade count, the equity-point count, the strategy,
and the date range. It explains the *historical* output; it never predicts
future returns or suggests parameter changes (no-AI boundary (b)).

Degrades to ``None`` (the run still stores the full result; the frontend shows
the report only) when the LLM is unavailable / over budget / refuses.
"""

from __future__ import annotations

import logging
from typing import Any

from workbench_api.services.explanation import STATUS_OK, ExplanationService

logger = logging.getLogger(__name__)

EXPLANATION_TASK = "backtest_explanation"

_REQUEST_LINE = (
    "In 1-2 sentences, explain why this backtest produced these results (the "
    "return, Sharpe and drawdown) for this strategy over this period, grounded in "
    "the real metrics and trade count shown. Do not predict future returns and do "
    "not suggest parameter changes."
)


def _build_grounding(
    *,
    strategy_id: str,
    metrics: dict[str, Any],
    trades_count: int,
    equity_points: int,
    start_date: Any,
    end_date: Any,
) -> tuple[str, set[str]]:
    """Render the grounding block + the citable token set (the real values the
    model may restate — already-computed historical numbers, not predictions)."""

    fields = {
        "STRATEGY": strategy_id,
        "DATE_RANGE_START": start_date,
        "DATE_RANGE_END": end_date,
        "CAGR": metrics.get("cagr"),
        "SHARPE": metrics.get("sharpe"),
        "MAX_DRAWDOWN": metrics.get("max_drawdown"),
        "TURNOVER": metrics.get("turnover"),
        "TRADES_COUNT": trades_count,
        "EQUITY_POINTS": equity_points,
    }
    grounding = "\n".join(f"{key}: {value}" for key, value in fields.items()) + "\n"
    citable = {str(value) for value in fields.values() if value is not None}
    return grounding, citable


def generate_backtest_explanation(
    explainer: ExplanationService | None,
    run: Any,
    mapped: dict[str, Any],
) -> str | None:
    """Return a grounded explanation for one backtest result, or ``None``.

    ``explainer is None`` (no LLM gateway — local / CI) → ``None``. A refusal /
    cost-guard trip / any exception also degrades to ``None`` so the worker
    always stores the full result."""

    if explainer is None:
        return None

    metrics = mapped.get("metrics") or {}
    params = run.params or {}
    grounding, citable = _build_grounding(
        strategy_id=getattr(run, "strategy_id", None) or "master_portfolio",
        metrics=metrics,
        trades_count=len(mapped.get("trades") or []),
        equity_points=len(mapped.get("equity") or []),
        start_date=params.get("start_date"),
        end_date=params.get("end_date"),
    )
    try:
        result = explainer.explain(
            task=EXPLANATION_TASK,
            grounding_text=grounding,
            citable=citable,
            request_line=_REQUEST_LINE,
        )
    except Exception as exc:  # noqa: BLE001 — degrade on budget/HTTP/anything
        logger.warning(
            "backtest_explanation_generation_failed",
            extra={"run_id": getattr(run, "run_id", "?"), "error": str(exc)},
        )
        return None

    return result.explanation if result.status == STATUS_OK else None
